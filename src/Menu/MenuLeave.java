    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int row = tblKaryawan.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(null, "Pilih data terlebih dahulu");
            return;
        }
        String kode = tblKaryawan.getValueAt(row, 0).toString();

        if (!checkStatusCuti(kode, "hapus")) {
            return;
        }

        int dialogButton = JOptionPane.YES_NO_OPTION;
        int dialogResult = JOptionPane.showConfirmDialog(null, "Konfirmasi hapus data?", "Warning", dialogButton);
        if (dialogResult == JOptionPane.YES_OPTION) {
            String sql = "DELETE FROM cuti WHERE id = '" + kode + "'";
            try {
                Statement stat = conn.createStatement();
                stat.executeUpdate(sql);

                loadData();

                JOptionPane.showMessageDialog(null, "Data Berhasil Dihapus");
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Data Gagal Dihapus " + e);
            }
        }

    }//GEN-LAST:event_btnDeleteActionPerformed

    private boolean checkStatusCuti(String kode, String Action) {

        try {
            // Cek status cuti terlebih dahulu
            String checkSql = "SELECT status FROM cuti WHERE id = ?";
            PreparedStatement checkStat = conn.prepareStatement(checkSql);
            checkStat.setString(1, kode);
            ResultSet rs = checkStat.executeQuery();

            if (rs.next()) {
                String status = rs.getString("status");

                if (!status.equalsIgnoreCase("NEW")) {
                    JOptionPane.showMessageDialog(null, "Hanya data dengan status NEW yang dapat di" + Action);
                    return false;
                }
            } else {
                JOptionPane.showMessageDialog(null, "Data tidak ditemukan.");
                return false;
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Data Gagal Dihapus: " + e.getMessage());
        }
        return true;
    }
