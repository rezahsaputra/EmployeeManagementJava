    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        try {
            PreparedStatement stat;

            // Ambil NIP dari input
            String nip = txtNIP.getText();
            int karyawanId = -1;

            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            String formattedMulai = sdf.format(dtpTglResign.getDate());
            
            if (!isEdit) {
                // Cek apakah NIP ada di tabel karyawan
                String checkSql = "SELECT id FROM karyawan WHERE nip = ?";
                stat = conn.prepareStatement(checkSql);
                stat.setString(1, nip);
                ResultSet rs = stat.executeQuery();

                if (rs.next()) {
                    // Dapatkan ID karyawan
                    karyawanId = rs.getInt("id");
                } else {
                    JOptionPane.showMessageDialog(null, "NIP tidak ditemukan di tabel karyawan.");
                    return; // hentikan proses jika NIP tidak ditemukan
                }

                // SQL insert setelah validasi NIP
                String sql = "INSERT INTO resign(karyawan_id, tanggal_pengajuan, tanggal_resign, alasan, status)"
                        + " VALUES (?, ?, ?, ?, ?)";
                stat = conn.prepareStatement(sql);
                stat.setInt(1, karyawanId);
                stat.setString(2, txtTglPengajuan.getText());
                stat.setString(3, formattedMulai);
                stat.setString(4, txtAlasan.getText());
                stat.setString(5, "NEW");
                stat.executeUpdate();

            } else {
                // SQL update
                String sql = "UPDATE resign SET tanggal_resign = ?, alasan = ? WHERE id = ?";
                stat = conn.prepareStatement(sql);
                stat.setString(1, formattedMulai);
                stat.setString(2, txtAlasan.getText());
                stat.setString(3, kodeOld); // ID penilaian lama untuk update
                stat.executeUpdate();
            }

            JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan");
            dispose();

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Data Gagal Disimpan: " + e.getMessage());
        }

    }//GEN-LAST:event_btnSaveActionPerformed

