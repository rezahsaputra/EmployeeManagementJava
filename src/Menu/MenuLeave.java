    private void loadData() {
        String sql;

        if (role.toLowerCase().equals("user")) {
            sql = "SELECT c.id,c.karyawan_id,c.tanggal_pengajuan,c.tanggal_mulai,c.tanggal_selesai,c.alasan,c.status,k.nip,k.nama"
                    + " FROM cuti c left join karyawan k on k.id = c.karyawan_id "
                    + " WHERE c.karyawan_id = '" + this.karyawan_id.toString() + "' "
                    + " ORDER BY id DESC";
        } else {
            sql = "SELECT c.id,c.karyawan_id,c.tanggal_pengajuan,c.tanggal_mulai,c.tanggal_selesai,c.alasan,c.status,k.nip,k.nama"
                    + " FROM cuti c left join karyawan k on k.id = c.karyawan_id "
                    + " ORDER BY id DESC";
        }
        Object[] Baris = {
            "id	",
            "NIP",
            "Nama",
            "Tanggal Pengajuan",
            "Tanggal Mulai",
            "Tanggal Selesai",
            "Alasan",
            "Status"
        };

        DefaultTableModel tabModel = new DefaultTableModel(null, Baris) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        tblKaryawan.setModel(tabModel);

        try {
            Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            int num = 1;

            while (hasil.next()) {
                String[] data = {
                    hasil.getString("id"),
                    hasil.getString("nip"),
                    hasil.getString("nama"),
                    hasil.getString("tanggal_pengajuan"),
                    hasil.getString("tanggal_mulai"),
                    hasil.getString("tanggal_selesai"),
                    hasil.getString("alasan"),
                    hasil.getString("status")
                };

                tabModel.addRow(data);
                num++;
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error saat memuat data penilaian karyawan:\n" + e.getMessage());
        }
    }

