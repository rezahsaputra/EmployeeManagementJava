    public DialogResignAddEdit(java.awt.Frame parent, boolean modal, Integer karyawanId) {
        super(parent, modal);
        initComponents();
        txtJudul.setText("Tambah Pengajuan Resign");
        this.karyawanId = karyawanId;
        String nip;
        try {
            String checkSql = "SELECT nip FROM karyawan WHERE id = ?";
            PreparedStatement stat;
            stat = conn.prepareStatement(checkSql);
            stat.setString(1, karyawanId.toString());
            ResultSet rs = stat.executeQuery();

            if (rs.next()) {
                // Dapatkan ID karyawan
                nip = rs.getString("nip");
            } else {
                JOptionPane.showMessageDialog(null, "NIP tidak ditemukan di tabel karyawan.");
                return; // hentikan proses jika NIP tidak ditemukan
            }

            txtNIP.setText(nip);

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Data Gagal Dimuat: " + e.getMessage());
        }
        setTanggal();
    }

    private void setTanggal() {
        Locale indo = new Locale("id", "ID"); // Lokalisasi Indonesia
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd", indo);
        String tanggalSekarang = sdf.format(new Date());
        txtTglPengajuan.setText(tanggalSekarang);
    }

    public void setData(String kode, String nip, String tglPengajuan, String tglResign, String alasan) {
        isEdit = true;
        kodeOld = kode;
        txtNIP.setText(nip);
        lblpengajkuan.setText(tglPengajuan);
        try {
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            Date parsedmulai = sdf.parse(tglResign);
            dtpTglResign.setDate(parsedmulai);
        } catch (ParseException e) {
            JOptionPane.showMessageDialog(null, "Format tanggal tidak valid: " + e.getMessage());
        }
        txtAlasan.setText(alasan);
        txtJudul.setText("Edit Pengajuan Resign");
    }

